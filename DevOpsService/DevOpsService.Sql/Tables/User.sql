DROP TABLE Sys_User;
GO
CREATE TABLE [dbo].[Sys_User](
	[ID] [uniqueidentifier] primary key NOT NULL,
	[UserId] [nvarchar](256) NOT NULL,	
	[Name] [nvarchar](256) NOT NULL,	
	[Password] [nvarchar](256) NOT NULL,	
	[Type] [nvarchar](256) NOT NULL,	
	[CompanyId] [nvarchar](256) NOT NULL,	
	[Phone] [nvarchar](256)  NULL,	
	[Email] [nvarchar](256)  NULL,	
	[Wechat] [nvarchar](256)  NULL,	
	[QQ] [nvarchar](256)  NULL,	
	[DingDingId] [nvarchar](256)  NULL,	
	[CreateTime] [datetime] NULL,
    [Creator] [nvarchar](256) NULL,
    [EditTime] [datetime] NULL,
    [Editor] [nvarchar](256) NULL,
	[F1] [nvarchar](256) NULL,
	[F2] [nvarchar](256) NULL,
	[F3] [nvarchar](256) NULL,
	[Enabled] [bit]  NOT NULL,
)
GO
